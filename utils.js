export function getSum(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

export function getSubtract(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}
export function getMultiply(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}
export function getDevide(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}

